/**
 * File: build-public-docs.mjs
 * Description: Compiles public Markdown source documents into static downloads and readable docs data.
 */
import { createHash } from 'node:crypto';
import { existsSync } from 'node:fs';
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

import { renderMarkdownDocumentToPdf } from './public-docs-pdf.mjs';

const repoRoot = process.cwd();
const manifestPath = path.join(repoRoot, 'docs/public/public-docs.manifest.json');
const downloadsDir = path.join(repoRoot, 'public/downloads');
const generatedDir = path.join(repoRoot, 'app/public/generated');
const versionRecordsDir = path.join(repoRoot, 'docs/public/version-records');
const generatedModulePath = path.join(generatedDir, 'public-docs.generated.ts');

function makeSlug(value) {
  return value
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80);
}

function hashContent(value) {
  return createHash('sha256').update(value).digest('hex').slice(0, 12);
}

function escapeTsString(value) {
  return JSON.stringify(value);
}

function normalizeTitleForComparison(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

function isDuplicateDocumentTitle(sourceTitle, documentTitle) {
  const source = normalizeTitleForComparison(sourceTitle);
  const target = normalizeTitleForComparison(documentTitle);

  return source === target || source.endsWith(target) || target.endsWith(source);
}

async function readJson(filePath) {
  const raw = await readFile(filePath, 'utf8');
  return JSON.parse(raw);
}

async function getMarkdownSourceFiles(documentConfig) {
  if (Array.isArray(documentConfig.sourceFiles) && documentConfig.sourceFiles.length) {
    return documentConfig.sourceFiles.map((sourceFile) => path.join(repoRoot, sourceFile));
  }

  if (!documentConfig.sourceDir) {
    throw new Error(`Public docs manifest entry "${documentConfig.slug}" needs sourceFiles or sourceDir.`);
  }

  const sourceDir = path.join(repoRoot, documentConfig.sourceDir);
  const entries = await readdir(sourceDir, { withFileTypes: true });

  return entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.md'))
    .map((entry) => path.join(sourceDir, entry.name))
    .sort((left, right) => left.localeCompare(right));
}

function removeFrontMatter(markdown) {
  if (!markdown.startsWith('---')) {
    return markdown;
  }

  const endIndex = markdown.indexOf('\n---', 3);
  if (endIndex === -1) {
    return markdown;
  }

  return markdown.slice(endIndex + 5).trimStart();
}

function normalizeSourceMarkdown(markdown, { documentTitle, documentSubtitle, isFirstFile }) {
  const lines = removeFrontMatter(markdown).replace(/\r\n/g, '\n').split('\n');
  let hasHandledLeadingTitle = false;
  let canSkipDuplicateSubtitle = false;

  return lines
    .map((line) => {
      const h1Match = line.match(/^#\s+(.+)$/);

      if (h1Match && !hasHandledLeadingTitle) {
        hasHandledLeadingTitle = true;
        const sourceTitle = h1Match[1].trim();

        if (isFirstFile && isDuplicateDocumentTitle(sourceTitle, documentTitle)) {
          canSkipDuplicateSubtitle = !!documentSubtitle;
          return '';
        }

        return `## ${sourceTitle}`;
      }

      if (!isFirstFile) {
        const nestedHeadingMatch = line.match(/^(#{2,5})\s+(.+)$/);

        if (nestedHeadingMatch) {
          const nextLevel = Math.min(nestedHeadingMatch[1].length + 1, 6);
          return `${'#'.repeat(nextLevel)} ${nestedHeadingMatch[2].trim()}`;
        }
      }

      if (canSkipDuplicateSubtitle) {
        if (!line.trim()) {
          return '';
        }

        if (line.trim() === documentSubtitle) {
          canSkipDuplicateSubtitle = false;
          return '';
        }

        canSkipDuplicateSubtitle = false;
      }

      return line;
    })
    .join('\n')
    .trim();
}

async function compileDocumentMarkdown(documentConfig) {
  const sourceFiles = await getMarkdownSourceFiles(documentConfig);
  const chunks = [];

  for (const [index, sourceFile] of sourceFiles.entries()) {
    if (!existsSync(sourceFile)) {
      throw new Error(`Public docs source file missing for "${documentConfig.slug}": ${sourceFile}`);
    }

    const raw = await readFile(sourceFile, 'utf8');
    const normalized = normalizeSourceMarkdown(raw, {
      documentTitle: documentConfig.title,
      documentSubtitle: documentConfig.subtitle,
      isFirstFile: index === 0,
    });

    if (normalized) {
      chunks.push(normalized);
    }
  }

  const subtitle = documentConfig.subtitle ? `\n\n${documentConfig.subtitle}` : '';

  return [
    '<!-- Generated by scripts/build-public-docs.mjs. Do not edit this compiled artifact directly. -->',
    `# ${documentConfig.title}${subtitle}`,
    ...chunks,
  ]
    .join('\n\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
    .concat('\n');
}

function stripInlineMarkdown(value) {
  return value
    .replace(/\[([^\]]+)]\(([^)]+)\)/g, '$1')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/\*\*([^*]+)\*\*/g, '$1')
    .replace(/__([^_]+)__/g, '$1')
    .replace(/\*([^*]+)\*/g, '$1')
    .trim();
}

function flushParagraph(paragraphLines, target) {
  if (!paragraphLines.length) {
    return;
  }

  const paragraph = stripInlineMarkdown(paragraphLines.join(' ').replace(/\s+/g, ' '));
  if (paragraph) {
    target.push(paragraph);
  }

  paragraphLines.length = 0;
}

function parseReadableDocument(markdown, documentConfig) {
  const lines = markdown.replace(/\r\n/g, '\n').split('\n');
  const intro = [];
  const sections = [];
  const paragraphLines = [];
  let currentSection = null;
  let parsedTitle = documentConfig.title;

  const getCurrentBody = () => (currentSection ? currentSection.body : intro);

  const startSection = ({ title, level }) => {
    flushParagraph(paragraphLines, getCurrentBody());

    currentSection = {
      id: makeSlug(`${documentConfig.slug}-${title}`),
      level,
      title,
      body: [],
    };

    const romanMatch = title.match(/^([IVXLCDM]+)\.\s+(.+)$/i);
    if (romanMatch) {
      currentSection.eyebrow = romanMatch[1].toUpperCase();
      currentSection.title = romanMatch[2].trim();
    }

    sections.push(currentSection);
  };

  for (const rawLine of lines) {
    const line = rawLine.trim();

    if (!line || line === '---' || line.startsWith('<!--')) {
      flushParagraph(paragraphLines, getCurrentBody());
      continue;
    }

    const h1Match = line.match(/^#\s+(.+)$/);
    if (h1Match) {
      flushParagraph(paragraphLines, getCurrentBody());
      parsedTitle = stripInlineMarkdown(h1Match[1]);
      continue;
    }

    const h2Match = line.match(/^##\s+(.+)$/);
    if (h2Match) {
      startSection({ title: stripInlineMarkdown(h2Match[1]), level: 2 });
      continue;
    }

    const h3Match = line.match(/^###\s+(.+)$/);
    if (h3Match) {
      startSection({ title: stripInlineMarkdown(h3Match[1]), level: 3 });
      continue;
    }

    const h4Match = line.match(/^####\s+(.+)$/);
    if (h4Match) {
      startSection({ title: stripInlineMarkdown(h4Match[1]), level: 4 });
      continue;
    }

    const h5Match = line.match(/^#####\s+(.+)$/);
    if (h5Match) {
      flushParagraph(paragraphLines, getCurrentBody());
      getCurrentBody().push(`#### ${stripInlineMarkdown(h5Match[1])}`);
      continue;
    }

    if (/^[-*]\s+/.test(line)) {
      flushParagraph(paragraphLines, getCurrentBody());
      getCurrentBody().push(`• ${stripInlineMarkdown(line.replace(/^[-*]\s+/, ''))}`);
      continue;
    }

    if (/^>\s+/.test(line)) {
      flushParagraph(paragraphLines, getCurrentBody());
      getCurrentBody().push(`> ${stripInlineMarkdown(line.replace(/^>\s+/, ''))}`);
      continue;
    }

    paragraphLines.push(line);
  }

  flushParagraph(paragraphLines, getCurrentBody());

  return {
    slug: documentConfig.slug,
    title: parsedTitle,
    subtitle: documentConfig.subtitle,
    version: documentConfig.version,
    updatedLabel: documentConfig.updatedLabel,
    intro: intro.filter((paragraph) => paragraph !== documentConfig.subtitle),
    sections: sections.filter((section) => section.title !== 'Source files'),
  };
}

function formatReadableDocument(document) {
  const lines = [
    `    slug: ${escapeTsString(document.slug)},`,
    `    title: ${escapeTsString(document.title)},`,
  ];

  if (document.subtitle) {
    lines.push(`    subtitle: ${escapeTsString(document.subtitle)},`);
  }

  if (document.version) {
    lines.push(`    version: ${escapeTsString(document.version)},`);
  }

  if (document.updatedLabel) {
    lines.push(`    updatedLabel: ${escapeTsString(document.updatedLabel)},`);
  }

  if (document.intro?.length) {
    lines.push('    intro: [');
    for (const paragraph of document.intro) {
      lines.push(`      ${escapeTsString(paragraph)},`);
    }
    lines.push('    ],');
  }

  lines.push('    sections: [');
  for (const section of document.sections) {
    lines.push('      {');
    lines.push(`        id: ${escapeTsString(section.id)},`);
    if (section.level) {
      lines.push(`        level: ${section.level},`);
    }
    if (section.eyebrow) {
      lines.push(`        eyebrow: ${escapeTsString(section.eyebrow)},`);
    }
    lines.push(`        title: ${escapeTsString(section.title)},`);
    lines.push('        body: [');
    for (const paragraph of section.body) {
      lines.push(`          ${escapeTsString(paragraph)},`);
    }
    lines.push('        ],');
    lines.push('      },');
  }
  lines.push('    ],');

  return `  ${escapeTsString(document.slug)}: {\n${lines.join('\n')}\n  }`;
}

function formatGeneratedModule({ readableDocuments, downloads }) {
  const documentEntries = readableDocuments.map(formatReadableDocument).join(',\n');
  const downloadEntries = Object.entries(downloads)
    .map(
      ([slug, value]) =>
        `  ${escapeTsString(slug)}: { markdown: ${escapeTsString(value.markdown)}, pdf: ${escapeTsString(value.pdf)} },`,
    )
    .join('\n');

  return `/**\n * File: public-docs.generated.ts\n * Description: Generated public document downloads and readable document data.\n *\n * Generated by scripts/build-public-docs.mjs. Do not edit by hand.\n */\nimport type { PublicReadableDocument } from '@app/public/content-types';\n\nexport const PUBLIC_DOC_DOWNLOADS = {\n${downloadEntries}\n} satisfies Record<string, { markdown: \`/downloads/\${string}\`; pdf: \`/downloads/\${string}\` }>;\n\nexport const PUBLIC_READABLE_DOCUMENTS = {\n${documentEntries}\n} satisfies Record<string, PublicReadableDocument>;\n`;
}

async function writeIfChanged(filePath, content) {
  let previous = null;

  if (existsSync(filePath)) {
    previous = await readFile(filePath, 'utf8');
  }

  if (previous === content) {
    return false;
  }

  await mkdir(path.dirname(filePath), { recursive: true });
  await writeFile(filePath, content, 'utf8');
  return true;
}

async function writeVersionRecord(documentConfig, markdown) {
  if (!documentConfig.versionRecords) {
    return null;
  }

  const recordDir = path.join(versionRecordsDir, documentConfig.slug);
  const hash = hashContent(markdown);

  await mkdir(recordDir, { recursive: true });
  const existingRecords = await readdir(recordDir).catch(() => []);
  const matchingRecord = existingRecords.find((entry) => entry.includes(`-${hash}.md`));

  if (matchingRecord) {
    return matchingRecord;
  }

  const timestamp = new Date().toISOString().replace(/[-:]/g, '').replace(/\.\d{3}Z$/, 'Z');
  const fileName = `${documentConfig.outputBaseName}-${timestamp}-${hash}.md`;
  await writeFile(path.join(recordDir, fileName), markdown, 'utf8');
  return fileName;
}

async function main() {
  const manifest = await readJson(manifestPath);
  const readableDocuments = [];
  const downloads = {};

  for (const documentConfig of manifest.documents) {
    if (!documentConfig.slug || !documentConfig.title || !documentConfig.outputBaseName) {
      throw new Error('Every public docs manifest entry needs slug, title, and outputBaseName.');
    }

    const markdown = await compileDocumentMarkdown(documentConfig);
    const downloadFileName = `${documentConfig.outputBaseName}.md`;
    const downloadPath = path.join(downloadsDir, downloadFileName);
    const pdfFileName = `${documentConfig.outputBaseName}.pdf`;
    const pdfPath = path.join(downloadsDir, pdfFileName);

    await writeIfChanged(downloadPath, markdown);
    await renderMarkdownDocumentToPdf({
      markdown,
      outputPath: pdfPath,
      title: documentConfig.title,
      subtitle: documentConfig.subtitle,
      version: documentConfig.version,
    });
    await writeVersionRecord(documentConfig, markdown);

    downloads[documentConfig.slug] = {
      markdown: `/downloads/${downloadFileName}`,
      pdf: `/downloads/${pdfFileName}`,
    };

    if (documentConfig.readable) {
      readableDocuments.push(parseReadableDocument(markdown, documentConfig));
    }
  }

  const generatedModule = formatGeneratedModule({ readableDocuments, downloads });
  await writeIfChanged(generatedModulePath, generatedModule);

  console.log(
    `Built ${readableDocuments.length} readable public docs and ${Object.keys(downloads).length} Markdown and PDF downloads.`,
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
